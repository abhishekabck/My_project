/* Q: Write a program to calculate the 
sum and product of Given two numbers
*/
package Java;

public class smaple1 {
    public static void main(String[] args){
        int a, b, sum = 0, product = 0;
        a = 10;
        b = 20;
        sum = a + b;
        product = a*b;
        // Prints the Sum of numbers
        System.out.println("Sum = " + sum);
        // Prints the Product of numbers
        System.out.println("Product = " + product);
    }
}
