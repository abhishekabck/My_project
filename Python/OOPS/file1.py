# Class is Bluprint for creating objects
# creating class (blueprint of student)
class Student:
    name = "Abhishek" # defining a constant or same name for every student
    id = 1001


# creating object (instance)
s1 = Student()
print(s1.name)
print(s1.id)

s2 = Student()
print(s2.name)
print(s2.id)

