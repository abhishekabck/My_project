/* Java Script contains following Data types:-
1. String -> Such as Name
2. Number -> Such as 234
3. Boolean -> Contains only True and False
4. Null -> The absence of Value
5. Undefined -> The value has yes to be assigned
6. BigInt -> A data type that accommodates a greater range of numbers.
7. Symbol -> Data type used as a unique identifier.
*/

// Strings
var name = 'Ravi';
console.log("Hello", name);
name = "Abhishek";
console.log(name);

// Numbers
var marks = 53;
console.log("Your Marks are " + marks);

// Boolean
var passed = true;
var is_occupied = false;
console.log(passed, is_occupied)

// BigInt
var number = 9453044981;
console.log(number);